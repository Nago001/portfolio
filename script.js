/* ==========================================================================
   State Management & Configuration
   ========================================================================== */

const carouselState = {
    phone: { current: 0, total: slideData.phone.length, timer: null },
    poster: { current: 0, total: slideData.poster.length, timer: null },
    website: { current: 0, total: slideData.website.length, timer: null }
};

// Staggered autoplay intervals to avoid all sliders updating at the same time
const autoplayIntervals = {
    phone: 6000,
    poster: 6000,
    website: 7000
};

/* ==========================================================================
   Initialization and Rendering
   ========================================================================== */

document.addEventListener('DOMContentLoaded', () => {
    // 1. Initialize Sticky Header scroll trigger
    const header = document.querySelector('.main-header');
    if (header) {
        window.addEventListener('scroll', () => {
            if (window.scrollY > 50) {
                header.classList.add('scrolled');
            } else {
                header.classList.remove('scrolled');
            }
        });
    }

    // 2. Render all carousels
    initCarousel('phone', 'phone-track');
    initCarousel('poster', 'poster-track');
    initCarousel('website', 'website-track');

    // 3. Set up autoplay
    startAutoplay('phone');
    startAutoplay('poster');
    startAutoplay('website');
});

/**
 * Initializes slides inside the carousel DOM track.
 */
function initCarousel(type, trackId) {
    const track = document.getElementById(trackId);
    if (!track) return;

    const slidesHtml = slideData[type].map((slide, index) => `
        <div class="carousel-slide" data-index="${index}">
            <img src="${slide.img}" alt="${slide.title}" loading="${index === 0 ? 'eager' : 'lazy'}">
        </div>
    `).join('');

    track.innerHTML = slidesHtml;

    // For phone mockup, we also initialize indicator dots
    if (type === 'phone') {
        const dotsContainer = document.getElementById('phone-dots');
        if (dotsContainer) {
            const dotsHtml = slideData.phone.map((_, index) => `
                <div class="dot ${index === 0 ? 'active' : ''}" onclick="goToSlide('phone', ${index})"></div>
            `).join('');
            dotsContainer.innerHTML = dotsHtml;
        }
    }

    // Load initial slide details (titles and description text)
    updateSlideText(type, 0);
}

/* ==========================================================================
   Carousel Navigation Functions
   ========================================================================== */

/**
 * Moves active index of specified carousel forward or backward by direction.
 */
function moveCarousel(type, direction) {
    resetAutoplay(type);
    
    let state = carouselState[type];
    state.current = (state.current + direction + state.total) % state.total;
    
    updateCarouselPosition(type);
}

/**
 * Jumps specified carousel directly to targetIndex.
 */
function goToSlide(type, targetIndex) {
    resetAutoplay(type);
    
    let state = carouselState[type];
    state.current = targetIndex;
    
    updateCarouselPosition(type);
}

/**
 * Updates slide position transform and triggers text updates.
 */
function updateCarouselPosition(type) {
    const state = carouselState[type];
    const track = document.getElementById(`${type}-track`);
    if (track) {
        track.style.transform = `translateX(-${state.current * 100}%)`;
    }

    // Update dots indicator if it's the phone mockup
    if (type === 'phone') {
        const dots = document.querySelectorAll('#phone-dots .dot');
        dots.forEach((dot, idx) => {
            if (idx === state.current) {
                dot.classList.add('active');
            } else {
                dot.classList.remove('active');
            }
        });
    }

    // Update standard slide counter display
    const counter = document.getElementById(`${type}-counter`);
    if (counter) {
        counter.textContent = `${state.current + 1} / ${state.total}`;
    }

    // Update detailed overlay descriptions
    updateSlideText(type, state.current);
}

/**
 * Updates details text (Title, Description) with a smooth fade animation.
 */
function updateSlideText(type, index) {
    const detailsContainer = document.getElementById(`${type}-details`);
    if (!detailsContainer) return;

    const titleElement = detailsContainer.querySelector('.slide-title');
    const descElement = detailsContainer.querySelector('.slide-desc');
    
    const nextData = slideData[type][index];
    if (!nextData) return;

    // Apply fade-out animation
    detailsContainer.style.transition = 'opacity 0.25s ease';
    detailsContainer.style.opacity = '0';

    // Also update mobile-only panel for phone carousel
    const mobilePanel = type === 'phone' ? document.getElementById('phone-mobile-details') : null;
    if (mobilePanel) {
        mobilePanel.style.transition = 'opacity 0.25s ease';
        mobilePanel.style.opacity = '0';
    }

    setTimeout(() => {
        if (titleElement) titleElement.textContent = nextData.title;
        if (descElement) descElement.textContent = nextData.desc;

        // Update URL button for website carousel
        if (type === 'website') {
            const urlBtn = detailsContainer.querySelector('.slide-url-btn');
            if (urlBtn) {
                if (nextData.url) {
                    urlBtn.href = nextData.url;
                    urlBtn.style.display = 'inline-flex';
                } else {
                    urlBtn.style.display = 'none';
                }
            }
        }

        // Sync mobile panel text
        if (mobilePanel) {
            const mobileTitleEl = mobilePanel.querySelector('.slide-title');
            const mobileDescEl = mobilePanel.querySelector('.slide-desc');
            if (mobileTitleEl) mobileTitleEl.textContent = nextData.title;
            if (mobileDescEl) mobileDescEl.textContent = nextData.desc;
            mobilePanel.style.opacity = '1';
        }

        // Apply fade-in animation
        detailsContainer.style.opacity = '1';
    }, 250);
}

/* ==========================================================================
   Autoplay Managers
   ========================================================================== */

function startAutoplay(type) {
    carouselState[type].timer = setInterval(() => {
        let state = carouselState[type];
        state.current = (state.current + 1) % state.total;
        updateCarouselPosition(type);
    }, autoplayIntervals[type]);
}

function resetAutoplay(type) {
    if (carouselState[type].timer) {
        clearInterval(carouselState[type].timer);
    }
    startAutoplay(type);
}
